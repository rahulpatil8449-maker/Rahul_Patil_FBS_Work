#1. &(AND)
#If both bits are 1 then consider 1 else consider 0
print(13 & 6)

#2. |(Or)
#If both bits are 0 then consider 0 else consider 1
print(13 | 6)

#3. ^(XOR)
# If both bits are different then consider 1 else 0
print(13 ^ 6)

#4. ~(NOT)
print(~ 10)    # -(10 +1)
print(~ 15)    # -(15 +1)

#5. <<(Left Shift)
print(5 << 2)
print(4 << 3)

#6. >>(Right Shift)
print(20 >> 2)
print(23 >> 3)
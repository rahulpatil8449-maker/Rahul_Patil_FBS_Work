#1. and
# If both operands are true then return true otherwise return false
print(True and True)
print(True and False)

#2. Or
# If both operands are false then return false otherwise return true
print(True or False)
print(False or False)

#3. not
# returns the reverse value/output/result
# Example if we're saying that something is not true than it'll return false(the reverse value of it)
# Example if we're saying that something is not false than it'll return true(the reverse value of it) 
print(not True)
print(not False)

####Relational/Comparison Operator

#1. ==(Exact equals to)
print(10 == 10)
print(10 == 20)

#2. !=(Not equals to)
print(10 != 20)
print(10 != 10)

#3. >(Greater than)
print(10>5)
print(10>15)

#4. >=(Greater than (or) equals to)
print(10 >= 10)
print(10 >= 15)

#5. <(Less than)
print(10 < 20)
print(10 < 10)

#6. <=(Less than (or) equals to)
print(10 <= 10)
print(10 <= 15)
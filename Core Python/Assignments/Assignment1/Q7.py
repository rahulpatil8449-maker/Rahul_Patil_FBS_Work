a = int(input("Enter the value of a: "))
b = int(input("Enter the value of b: "))
c = int(input("Enter the value of c: "))

r1 = -b + ((b ** 2 - 4 * a * c)) * 0.5
r2 = -b - ((b ** 2 - 4 * a * c)) * 0.5

print("The Quadratic Equation value of r1 will be:", r1)
print("The Quadratic Equation value of r2 will be:", r2)